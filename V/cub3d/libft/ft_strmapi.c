#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	len;
	size_t	i;
	char	*final;

	if (!s)
		return (NULL);
	len = ft_strlen(s);
	i = 0;
	final = (char *)malloc(sizeof(char) * (len + 1));
	if (!final)
		return (NULL);
	while (s[i] != '\0')
	{
		final[i] = f(i, s[i]);
		i++;
	}
	final[i] = '\0';
	return (final);
}
